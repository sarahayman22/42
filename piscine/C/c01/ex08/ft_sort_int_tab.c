#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	flag;
	int	temp;

	i = 0;
	flag = 0;
	while (!flag)
	{
		flag = 1;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				flag = 0;
			}
			i++;
		}
	}
}
