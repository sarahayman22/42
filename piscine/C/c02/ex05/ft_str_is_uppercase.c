int	ft_str_is_uppercase(char *str)
{
	int	count;
	int	i;

	i = 0;
	count = 1;
	while (str[i])
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			count = 0;
		}
		i++;
	}
	return (count);
}
